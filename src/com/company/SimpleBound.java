public class SimpleBound {
    private int x;
    private int y;
    private int c;

    public SimpleBound(int x, int y, int c) {
        this.x = x;
        this.y = y;
        this.c = c;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getC() {
        return c;
    }
}
